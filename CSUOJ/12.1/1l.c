int ispalin(int num)
{
    int arr[5];
    int k=0;
    while ( num != 0 )
    {
        arr[k++] = num % 10;
        num /= 10;
    }
    if ( k%2 == 0 )
    {
        int i = 0, j = k - 1;
        if ( k==2 ) 
        {
            if ( arr[i] == arr[j] ) return 1;
        }
        do
        {
            if (arr[i] != arr[j]) return 0;
            i++;j--;
        } while (i!=j-1);
        
    }
    else
    {
        for (int i = 0, j = k - 1; i != j; i++, j--)
        {
            if (arr[i] != arr[j])
                return 0;
        }
    }
    
    return 1;

}

